# 练习：
#   1. 创建文件夹myfiles
mkdir myfiles
#   2. 在myfiles文件夹内创建文件：
#     a.mp3 ab.txt ac.mp3 abc.txt aabb.mp3 bc.txt cd.mp3
cd myfiles
touch a.mp3 ab.txt ac.mp3 abc.txt aabb.mp3 bc.txt cd.mp3
#   3. 列出所有以.mp3结尾的文件
ls *.mp3
#   4. 删除.之前只有一个字符的文件
rm ?.*
#   5. 列出含有c这个字符的文件
ls *c*
#   6. 删除myfiles文件内所有的mp3文件
rm *.mp3

